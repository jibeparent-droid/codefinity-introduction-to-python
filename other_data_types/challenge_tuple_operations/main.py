print (f"Number of Apples: {apple_count}")
banana_index = shelf.index("bananas")
print (f"First Banana index: {banana_index}")
if apple_count < 5:
    print ("Apples need to be restocked.")
else:
    print ("Apples are sufficienty stocked.")
